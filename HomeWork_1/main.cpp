#include<iostream>
using namespace std;

int Factorial(int n);


void main()
{
	setlocale(LC_ALL, "");
	int n;
	cout << "Введите число для вычесления Факторила: "; cin >> n;
	cout << n << " ! " << " = " << Factorial(n) << endl;
	int factorial(int n);
	{
		int a = 1;
		for (i = 1; i <= n; i++)
		{
			a *= i;
			return a;
		}
	}
}